package br.com.ecosolucoes.ecotroca.models;


public class Cidadao {
    
    private int id;
    private int idPessoa;
    private double saldoPontuacao;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdPessoa() {
        return idPessoa;
    }

    public void setIdPessoa(int idPessoa) {
        this.idPessoa = idPessoa;
    }

    public double getSaldoPontuacao() {
        return saldoPontuacao;
    }

    public void setSaldoPontuacao(double saldoPontuacao) {
        this.saldoPontuacao = saldoPontuacao;
    }
    
    
    
    
}
